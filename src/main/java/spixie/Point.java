package spixie;

public class Point {
    private double x;
    private double y;

    public double gravity_x = 0.5;
    public double gravity_y = 0.5;

    public Point next = null;
    public Point prev = null;

    public Point(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

    @Override
    public String toString() {
        return "Point{" +
                "x=" + x +
                ", y=" + y +
                '}';
    }
}
